public class Principale {

    public static void main(String[] args) {
        System.out.println("Début programme");

        int i = 0;

        /* Opérateurs possibles */
        // && pour et
        // || pour ou
        // ! pour non

        /* Tant que */
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        /* Pour */
        for (int index = 0; index < 10; index++) {
            System.out.println(index);
        }
    }

}
